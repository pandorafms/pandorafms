<h1>Tarea de reconocimiento (<i>recon</i>)</h1>

<p>
Para crear una tarea <i>recon</i> debe rellenar este formulario. Rellene todos los campos, poniendo especial atencióna <b>Servidor de exploración de red</b>; recuerde que debe elegir un servidor de exploración de red (<i>recon</i>) para que la tarea se asigne al servidor necesario y se ejecute correctamente.
</p>
<p>
&laquo;Red&raquo; se refiere al segmento de red, y se definen usando el formato <b>CIDR</b>. Por ejemplo, 192.168.1.0/24 se refiere a <i>toda la clase C 192.168.1.0</i>, que incluye todas las direcciones en el rango: 192.168.1.0 - 192.168.1.255.
</p>
<p>
El &laquo;Perfil de red&raquo; proporciona perfiles definidos para la tarea de reconocimiento. Añadirá tales módulos definidos en el perfil a las máquinas descubiertas en la red.
</p>